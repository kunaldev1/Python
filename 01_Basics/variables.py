# variables.py

age = 17 #Integer Data Type
height = 5.8 #Floating Data Type
name = "Kunal" #String Data Type
is_student = True #Boolean Data Type(Either true or false)

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)

# Updating variables
age = 19
print("Updated Age:", age)

# Multiple assignments
x, y, z = 10, 20, 30

print("x =", x)
print("y =", y)
print("z =", z)

#Output:
# Name: Kunal
# Age: 17
# Height: 5.8
# Student: True
# Updated Age: 19
# x = 10
# y = 20
# z = 30
